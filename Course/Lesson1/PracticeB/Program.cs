Console.Write("Введите число а: ");
float numA = float.Parse(Console.ReadLine());
Console.Write("Введите число b: ");
float numB = float.Parse(Console.ReadLine());

Console.WriteLine($"Сумма чисел равна {numA + numB}");
Console.WriteLine($"Разность чисел равна {numA - numB}");
Console.WriteLine($"Произведение чисел равно {numA * numB}");
Console.WriteLine($"Частное чисел равно {numA / numB}");